//iterables are iterable objects
//iterables can be accessed with simple and efficient code.
//iterables can be iterated over with for . . of loops.
//Example: iterating a string
const Name = "Rifat"
for(const x of Name){
    console.log(x);
}

//Example: iterating an array
const num = [1, 2, 3]
for(const x of num){
    console.log(x);
}